# 3차시 - 문자열

# 실습 A
# 자기소개 프로그램
# "이름: "뒤에 입력하는 값을 name에 저장
name = input("이름: ")
# "나이: "뒤에 입력하는 값을 정수로 age에 저장
age = int(input("나이: "))
# "취미: "뒤에 입력하는 값을 hobby에 저장
hobby = input("취미: ")

# "안녕하세요!" 출력
print(f"안녕하세요!")
# "저는 name이고, age살입니다." 출력
print(f"저는 {name}이고, {age}살입니다.")
# "취미는 hobby입니다!" 출력
print(f"취미는 {hobby}입니다!")

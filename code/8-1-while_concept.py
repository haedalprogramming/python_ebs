# continue 복습 예제
# n에 0저장
n = 0
# n이 5보다 작은 경우에만 반복
while n < 5:
    # n값 1증가
    n += 1
    # 만약 n값이 3이라면
    if n == 3:
		# 건너뛰기
        continue
	# n출력
    print(n)

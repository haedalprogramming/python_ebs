#while True 예제 3
# 숫자 수집기
# numbers에 빈 리스트 저장
numbers = []
# 무한 반복
while True:
	#"숫자 입력 (q=종료): " 뒤에 입력되는 값을 num에 저장
    num = input("숫자 입력 (q=종료): ")
	# 만약 num값이 'q'라면
    if num == 'q':
		# 반복 중단
        break
	# numbers리스트에 변수 num을 정수로 추가
    numbers.append(int(num))
# numbers 리스트 출력
print(f"입력한 숫자: {numbers}")
# numbers 리스트의 합계 출력
print(f"합계: {sum(numbers)}")

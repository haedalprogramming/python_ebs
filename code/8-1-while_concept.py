# while-else 예제
# n에 3저장
n = 3
# n이 0보다 큰 경우에만 실행
while n > 0:
	# n출력
    print(n)
	# n 1감소
    n -= 1
# 아니라면 (while 반복문이 break없이 종료됐다면)
else:
	# "정상 종료!" 출력
    print("정상 종료!")

#while True 예제 4
# 양수만 입력받기
# 무한 반복
while True:
	# "양수를 입력: " 다음에 입력되는 값을 정수로       n에 저장
    n = int(input("양수를 입력: "))
	# 만약에 n이 0보다 크다면
    if n > 0:
		# n입력 완료!
        print(f"{n} 입력 완료!")
		# 반복 중단
        break
	# "양수가 아닙니다. 다시!" 출력
    print("양수가 아닙니다. 다시!")

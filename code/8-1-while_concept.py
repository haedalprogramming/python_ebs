# while True 예제 1 
# 비밀번호 확인 프로그램
# password 변수에 "1234"저장
password = "1234"
# 무한 반복:
while True:
	# "비밀번호: "다음에 입력되는 값을 pw변수에 저장
    pw = input("비밀번호: ")
	# pw값과 password 값이 같다면
    if pw == password:
		# "로그인 성공!" 출력
        print("로그인 성공!")
		# 반복 중단
        break
	# "틀렸습니다. 다시 시도하세요." 출력
    print("틀렸습니다. 다시 시도하세요.")
